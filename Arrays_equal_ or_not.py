# Write a program in Python for, How to compare two array is equal in size or not.

# Program 1: Conditional Expression Using len() Function
arr1=[1,2,3,4,5]
arr2=[1,3,4,5,7]
if len(arr1) == len(arr2):
    print("arrays are equal in size")
else:
    print("arrays are not equal in size")

# Program 2: Conditional Expression Using len() Function

